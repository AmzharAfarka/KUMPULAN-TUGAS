let a = 8.3;
let c = "3.2";

let cNum = parseFloat(c);

console.log(`${a} + ${cNum} = ${(a + cNum).toFixed(1)}`);
console.log(`${a} - ${cNum} = ${(a - cNum).toFixed(1)}`);
console.log(`${a} x ${cNum} = ${(a * cNum).toFixed(2)}`);
console.log(`${a} / ${cNum} = ${(a / cNum)}`);
console.log(`${parseInt(a)} % ${parseInt(cNum)} = ${parseInt(a) % parseInt(cNum)}`);